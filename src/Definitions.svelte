    <div>
        <h2>Definition von Exklusion</h2>
        <p>
            Prof. Dr. Johannes Schütte von der Technischen Hochschule Köln
            versteht unter sozialer Exklusion, dass Menschen von Funktionen in
            der Gesellschaft, die sie anstreben und die nach gängigen
            Normalvorstellungen auch jedem Menschen frei zugänglich sind,
            ausgeschlossen werden (Schütte 2012: 105). Das ist zum Beispiel die
            Teilnahme am sozialen Leben in heutiger Zeit etwa im Autokino oder
            bei Musikveranstaltungen. Folglich meint Exklusion den Ausschluss
            von Tätigkeiten und sozialer, bzw. gesellschaftlicher, Teilnahme,
            die als alltäglich und jedem frei zugänglich gelten und daher
            prinzipiell für jeden Menschen möglich sein sollten.
        </p>
    </div>

    <div>
        <h2>Definition von Inklusion</h2>
        <p>
            Die UN-Behindertenrechtkonvention definiert Inklusion als
            Möglichkeit der umfassenden gleichberechtigten und selbstbestimmten
            Teilhaber aller Menschen am gesellschaftlichen Leben (vgl.
            Bundesministerium für Familie, Senioren, Frauen und Jugend o.J: 5).
            Im allgemeinen Sprachgebrauch wir der Begriff Inklusion häufig
            synonym mit Integration verwendet. Beide Begriffe sind jedoch
            voneinander zu unterscheiden. Nach Georg Feuser etablierten sich
            diese Begriffe in der Soziologie im 19. und 20. Jahrhundert.
            Integration leite sich vom Lateinischen „integer“ ab und stehe für
            „die Wiederherstellung eines Ganzen“ oder „die Verbindung einer
            Vielfalt zu einer Einheit“. Der Begriff Inklusion habe einen
            ähnlichen Wortursprung und bedeute „einschließen, einschließlich,
            inbegriffen“ und stehe für eine „Ganzheit“. Folglich würde
            Integration einen Prozess und Inklusion einen Zustand beschreiben.
            Integration könne somit als Weg zur Inklusion verstanden werden
            (vgl. Feuser 2013: 1). Der Soziologe Prof. Dr. Clemens Dannebeck
            beschreibt im Jahr 2013 Inklusion als eine „gesellschaftliche
            Herausforderung“. Inklusive Entwicklung könne nur gelingen, wenn
            „strukturelle Veränderungen zum Abbau vorhandener oder zur
            Verhinderung von neu errichteter Teilhabebarrieren führen und diese
            Prozesse von (institutionellen oder personellen)
            Entscheidungsträgern gestaltet und vollzogen werden, die ihr
            soziales Handeln einer anhaltenden Reflexion in Bezug auf die darin
            enthaltenen Inklusions- und Exklusionspotenziale unterziehen“
            (Dannebeck 2013: 52). Dies bedeutet, die Teilhabe von Menschen am
            gesellschaftlichen Leben wird nicht nur durch physische Barrieren,
            sondern auch durch haltungsbedingte Barrieren, das sind Haltungen,
            Vorstellungen und Überzeugungen von und gegenüber Menschen,
            beschränkt. Institutionen oder andere Entscheidungsträger müssen
            daher stets jede ihrer Entscheidungen daraufhin hinterfragen, ob
            Menschen oder Gruppen inkludiert oder ausgeschlossen werden. Daher
            solle die Umsetzung der Ziele mit Blick auf Inklusion stetig
            reflektiert werden.
        </p>
