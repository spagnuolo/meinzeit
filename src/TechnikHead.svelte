        <span>Technische Umsetzung und künstliche Intelligenz</span>
        <br />
        Technische Umsetzung und künstliche Intelligenz Autonom ist nicht gleich
        künstliche Intelligenz. Die ältere Generation der autonomen Fahrzeuge nutzte
        noch algorithmische Lösungsansätze. Das heißt das alle möglichen Handlungen
        und Entscheidungen vorher fest und manuell einprogrammiert werden mussten.
    </p>
    <p>
        Um damals autonom zu Fahren arbeiteten Echtzeitsysteme zusammen, die für
        Lokalisierung, Umgebungswahrnehmung, Planung und Steuerung zuständig
        waren. Ein großes Problem war die Planung und Vorgehensweise bei
        Unklarheit. Eine Maschine mit algorithmischen Lösungsansätzen besitzt
        nur die einprogrammierte tiefe Domänenlogik. Sie kann nicht aus dieser
        Logik ausbrechen. Wir Menschen mit unserer Weltenlogik sind dort viel
        adaptiver auf Unklarheit vorbereitet. Dem entsprechend wird Künstliche
        Intelligenz eingesetzt, zum Beispiel für das Problem der Objekterkennung
        und Klassifizierung von Objekten und Personen. In den letzten dutzend
        Jahren hat die Forschung durch jede Menge Testfahrten Daten gesammelt.
        Mit diesen Daten haben sie dann K.I. Modelle trainiert und verbessert,
        bis sie eine 99.99999% Genauigkeit erreichte und so die
        Vollautomatiesierungspflicht eingeleitet hat.
