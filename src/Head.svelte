<h2>Autonomes Fahren: Forschungsfrage hier?</h2>
<div>
    <p class="block">
        In unserer zweiten diesjährigen Ausgabe soll es um die bis jetzt
        mitunter größte Errungenschaft des 21. Jahrhunderts gehen: Das autonome
        Fahrzeug. Noch vor 10 Jahren war der Verkehr in Deutschland geprägt von
        umwelt-schädlichen Verbrennungsmotoren, einer unvernetzten Infrastruktur
        und Indivi-dualbesitz. Anfang des Jahres 2020 waren noch insgesamt 58,2
        Millionen Kraftfahrzeuge in Deutschland zu-gelassen und die
        Verkehrsemissionen lagen bei circa 163,5 Millionen Tonnen CO<sub>2</sub
        >. Selten fuhren elektrobetriebene Fahrzeuge auf den Straßen und für den
        Betrieb autonomer Fahrzeuge im öffentlichen Verkehr brauchte es eine
        Sondererlaubnis.
        <br />
        <br />
        Bis zum Jahr 2030 wandelte sich, mit dem autonomen Fahrzeug der Stufe fünf,
        die Rolle der Menschen als Fahrer*innen zu Passagier*innen ohne Fahraufgabe.
    </p>
    <p class="block">
        Der Individualbesitz wurde abgeschafft, sodass sich verschiedene
        Personen-gruppen, wie Familien oder benachbarte Wohneinheiten ein
        autonomes Fahrzeug teilen. Des Weiteren herrscht nun eine
        Vollautomatisierungspflicht. Demnach sind keine nicht-automatisierten
        Fahr-zeuge auf den Straßen erlaubt, um eine zentralvernetzte
        Infrastruktur, die alle Verkehrsteilnehmer*innen miteinander
        kommunizieren lässt, zu ermöglichen. Der öffentliche Nahverkehr wurde
        auf autonome Bahnen und Reisebusse reduziert, die nur noch weite
        Strecken anfahren.
        <br />
        <br />
        Dadurch hat sich viel verändert. Unter anderem sind mehr Menschen von der
        Stadt zurück aufs Land gezogen, da der ländliche Lebensraum, aufgrund der
        gestiegenen Mobilität an Attraktivität gewonnen hat. Unsere CO<sub
            >2</sub
        >-Emissionen konnten durch die Elektromobilität stark
    </p>
    <p>
        gesenkt werden. Durch die vernetzte Infrastruktur gibt es keine Staus
        mehr und der Verkehr ist sicherer geworden, sodass auch die Polizei
        diesbezüglich entlastet werden konnte.
        <br />
        <br />
        Außerdem hat sich der Arbeitsmarkt umgeschichtet: Manche Arbeitsplätze, wie
        die der Fahrlehrer*innen, Bus-fahrer*innen oder Taxifahrer*innen sind verloren
        gegangen, andere Arbeitsplätze haben sich gewandelt, wie die der Auto Mechatroniker*innen
        oder Automobil-hersteller*innen und wieder andere Arbeitsplätze sind neu
        entstanden, wie die der 3D-Druck-Expert*innen oder der Data Stewards. Und
        das sind nur ein paar der Auswirkungen des autonomen Fahrens. Wir als Redaktion
        stellen uns die Frage, wie sich die Gesellschaft mit dieser neuen Errungenschaft
        arrangiert hat und wie exklusiv das Modell des autonomen Fahrzeugs ist.
    </p>
</div>

