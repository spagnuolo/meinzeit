<h2>Elaine Herzberg</h2>
<p>
    Elaine Herzberg Der erste fatale Unfall mit einem selbstfahrenden Auto fand
    2018 im Bundesstaat Arizona statt. Das Fahrzeug der Firma Uber führte eine
    Testfahrt durch und war mit einem Fahrer bemannt, der im Notfall jederzeit
    eingreifen kann. Elaine Herzberg, 49 Jahre, lief mit ihrem Fahrrad am
    Straßenrand. Das Wetter war klar und trocken. Mensch und Maschine waren
    nicht in der Lage den Unfall zu verhindern, so nach Begutachtern.
</p>
<p>
    Und das ist auch was passieren wird. K.I. Systeme sind so Komplex, dass es
    sehr schwierig ist sie vollständig auf Sicherheit und Fehler zu prüfen und
    Schwachstellen oder blinde Stellen zu finden. Für Personen die Algorithmen
    für maschinelles Lernen nutzen, daran forschen oder sich damit
    auseinandersetzen, sind solche Unfälle nicht überraschend. Grundsätzlich
    verhalten K.I. Systeme sich in einem bekannten Prozentsatz der Zeit wie
    erwartet. Wenn dies nicht der Fall ist, ist das Resultat nicht nur eine
    kleine Abweichung vom erwarteten Ergebnis, sondern ein sehr unerwartetes.
</p>
<p>
    Jeden Tag sterben Menschen bei Autounfällen. Wir haben uns daran gewöhnt,
    das zu rationalisieren: "Sie wurden von einem betrunkenen Fahrer
    angefahren", "Sie schrieb eine SMS", "Sie haben die Ampel übersehen" usw.
    Dies sind "normale" Gründe für schlimme Unfälle und wir können sie nicht nur
    rationalisieren, sondern es auch so zu rationalisieren, wie es uns selbst
    nicht passieren würde: "Ich fahre nicht betrunken", "Ich schreibe nicht
    [viel], während ich fahre", "Ich passe auf". Aber diese Algorithmen werden
    nicht so versagen. Jeder Unfall wird einzigartig und seltsam und
    beängstigend sein. Es würde uns nicht wundern, wenn jemand irgendwann ein
    extremes Outfit trägt und das autonome Auto denkt, dass die Person ein Teil
    der Straße ist und versucht, die Person explizit zu überfahren. Oder wenn
    das Auto plötzlich entscheidet, dass die Straße in einem 90 Grad Winkel
    verläuft und von einer Brücke fährt. Oder dass die Spritzer aus einer Pfütze
    vorne tatsächlich ein entgegenkommendes Auto ist und in Passanten ausweichen
    muss.
</p>
<p>
    Es wird immer tragisch, und unvorhersehbar sein. Wir werden also insgesamt
    eine viel geringere Anzahl von Todesfällen haben, die durch selbstfahrende
    Autos verursacht werden, aber diejenigen, die passieren, werden völlig
    unerwartet und beängstigend sein. Dies können wir auch nicht verhindern,
    ohne die Autos auf Schienen zu setzen.
</p>

