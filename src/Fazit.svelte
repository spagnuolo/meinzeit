<div class="container">
    <h2>Redaktionelles Fazit: Autonome Mobilität ein inklusives Modell</h2>
    <p>
        Blicken wir als Redaktion der Meinzeit zurück auf 10 Jahre Geschichte
        der autonomen Mobilität so, sehen wir zunächst das Innovationspotential
        und die Chance, unsere Alltagswelt inklusiver zu gestalten.
    </p>
    <p>
        Autonomes Fahren und die damit verbundene KI kann ein „neuer
        Meilenstein“ des menschlichen Fortschritts sein, bürgt aber auch
        Gefahren unsere Gesellschaft weiter in Menschen mit und in Menschen ohne
        Ressourcen zu spalten und zu separieren. Um dieser Entwicklung entgegen
        zu wirken, bedarf es Konzepte und Ideen, die eine Philosophie vorgeben,
        die alle berücksichtigt und absolute Inklusion gewährleistet. Das
        bedeutet Themenfelder wie Justiz, Industrie und Wirtschaft, Politik und
        Soziologie zu einen, um erfolgreich Mensch und Maschine eine harmonische
        und lebenswerte Zukunft zu ermöglichen. Für diese Ziele müssen Gesetze
        jedoch noch folgende Sachverhalte neu regeln: Prüfverfahren für die
        Erteilung einer Betriebserlaubnis für Kraftfahrzeuge mit autonomen
        Fahrfunktionen, Pflichten der beteiligten Personen, die
        Datenverarbeitung und Schaffung von einheitlichen Vorschriften zur
        Ermöglichung der Erprobung von automatisierten und autonomen
        Kraftfahrzeugen. Bereits heute ist gewiss, dass die Autonomisierung des
        Fahrens, in Bezug auf aufgeworfene ethische Fragen einen großen Einfluss
        haben wird. Eine Reihe, der von der Ethik-Kommission 2017 gestellten
        ethischen Fragen zum autonomen Fahren der Stufe 5 sind in Zukunft noch
        zu beantworten. Die Vollautomatisierung der Mobilität, wird
        einschneidende Folgen für die persönliche Freiheit der Menschen haben,
        wenn durch Technik subjektiv gewährte Sicherheit über die persönliche
        Handlungs- und Entscheidungsfreiheit der Bürger gestellt wird. Dies
        birgt die Gefahr, Menschen, die den Wandel nicht mitmachen wollen und
        kritischer bewerten mit der Zeit abzuhängen und zu exkludieren. Fragen
        der Haftung, zum Beispiel bei Unfällen, sind zu diskutieren. Erst dann
        könnte aus ethischer Sicht das autonome Fahren einen Beitrag zu einer
        inklusiveren Gesellschaft leisten. Wichtig ist auch Berufsausbildungen
        an den technischen Wandel zu orientieren und daran anzupassen. Für
        Menschen, die bereits länger in ihrem Beruf tätig sind oder deren
        Berufszweig wegfällt, sind attraktive Umschulungsangebote oder
        Weiterbildungen anzubieten, die durch staatliche Zuschüsse finanziert
        werden.
    </p>
    <p>
        Autonome Fahrzeuge müssen demnach kein exklusives Zukunftsmodell sein.
        Die Möglichkeiten zur Inklusion innerhalb der fortschreitenden
        Entwicklung bieten eine Menge Potenzial. Solange die aufgeführten
        Risiken bedacht, und bei der Entwicklung zukünftiger Mobilitätsmodelle
        berücksichtigt werden, beziehungsweise diesen entgegengewirkt wird, und
        aufkommende Chancen möglichst umgesetzt werden, so sind wir als
        Redaktion uns sicher, ist die autonome Mobilität der heutigen Mobilität
        in Bezug auf die Teilhabe exkludierter Menschengruppen weit voraus. Ihr
        könnt Euch schon auf unsere dritte Ausgabe freuen. Dann werfen wir ein
        Blick auf heutiges urbanes Smart-Living und die Frage, ob Smarthomes
        eine Möglichkeit darstellen, ein langes Wohnen innerhalb der eigenen
        vier Wänden zu ermöglichen.
    </p>
</div>

